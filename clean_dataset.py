import pandas as pd

df = pd.read_csv("SpotifyFeatures.csv")
print(df.shape)
df.head()

df = df[['track_name', 'artist_name', 'genre', 'track_id', 'popularity',
         'valence', 'energy', 'danceability', 'tempo']]

df.dropna(inplace=True)
df.drop_duplicates(subset=['track_name', 'artist_name'], inplace=True)

df['text'] = df['track_name'] + " by " + df['artist_name'] + " [" + df['genre'] + "]"

df.to_csv("clean_tracks.csv", index=False)

print(f"Saved cleaned dataset with {len(df)} tracks to clean_tracks.csv")

